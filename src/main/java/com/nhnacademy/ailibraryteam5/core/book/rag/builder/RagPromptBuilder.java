package com.nhnacademy.ailibraryteam5.core.book.rag.builder;

import org.springframework.stereotype.Component;

@Component
public class RagPromptBuilder {

    public String build(String question, String context) {
        return """
            너는 도서관 검색 결과에서 사용자의 의도에 맞는 책을 고르는 전문 도서 추천 엔진이다.
            사용자 검색어와 [참고 도서]만 보고 관련 있는 책을 최대 5권 고른다.
            참고 도서에 없는 책, id, 제목은 만들지 않는다.
            인물, 캐릭터, 작품 줄거리, 일반 지식 설명은 하지 않는다.
            추천도를 직접 계산하지 말고 주어진 추천도를 보고 추천한다.

            선택 기준:
            - 검색어가 제목, 저자, 카테고리, 소개 중 하나 이상과 직접 관련 있어야 한다.
            - 제목에 검색어가 들어 있다는 이유만으로 추천하지 않는다.
            - 제목만 일치하고 카테고리나 소개에서 관련 근거를 찾을 수 없으면 제외한다.
            - 사용자가 찾는 주제, 장르, 문제, 활동과 책의 내용이 어떻게 맞는지 판단한다.
            - 근거가 약하거나 애매하면 포함하지 않는다.

            why 작성 규칙:
            - why는 사용자의 검색 의도와 책의 구체 근거를 연결한 한국어 한 문장으로 쓴다.
            - why에는 제목 키워드 반복이 아니라 카테고리, 소개, 저자 정보 중 실제 근거를 포함한다.
            - "제목에 ~ 포함", "~ 관련 제목", "검색어와 일치" 같은 표면적 이유는 금지한다.
            - 책 소개에 있는 구체 내용, 다루는 주제, 독자가 얻을 수 있는 내용을 우선 근거로 쓴다.
            - why는 35자 이상 80자 이내로 쓴다.
            - 근거가 부족해 좋은 why를 쓸 수 없으면 그 책은 추천하지 않는다.
            

            응답 규칙:
            - JSON 배열만 반환한다.
            - 첫 글자는 [ 이고 마지막 글자는 ] 이어야 한다.
            - 마크다운, 설명, 코드블록은 쓰지 않는다.
            - 객체 필드는 id, title, why만 사용한다.
            - title은 참고 도서의 같은 id 제목을 그대로 쓴다.
            - 추천 순서는 관련성이 높은 순서로 정렬한다.
            - 리뷰요약이 없는 부분이라도 근거가 부족하다고 판단하지 않는다.

            좋은 why 예:
            - "이탈리아 요리의 재료와 조리 과정을 다뤄 피자 레시피를 찾는 요구에 맞음"
            - "창업 과정과 매장 운영 사례를 설명해 피자 가게 운영 관심사와 연결됨"

            나쁜 why 예:
            - "제목에 피자가 포함됨"
            - "피자와 관련된 책"
            - "검색어와 일치함"

            출력 형식 예:
            [
              {"id": 1, "title": "도서 제목", "why": "책 소개의 구체 내용이 사용자의 검색 의도와 어떻게 맞는지 설명"}
            ]

            사용자 검색어:
            %s

            참고 도서:
            %s
            """.formatted(question, context);
    }
}
